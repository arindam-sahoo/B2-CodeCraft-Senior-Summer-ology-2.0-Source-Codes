# Functions
# Definition: Funtions are block of code that gets executed when called.
# Syntax to define a dunction: def function_name(parameters):


def greet(name):  # Definition | Formal Parameter
  print("Good Morning!!", name)


greet("Reyansh")  # Calling a function | Actual Parameter
greet("Aarna")
greet("Niranjan")

# def --> It is a keyword used to start a function or define a function.
# greet --> It is the name of the function.
# name --> Parameter Received.

# Parameters
# Actual Parameters -- The values that are passed to the function. It is written in the calling statement.
# Formal Parameters -- The parameters that are defined in the function. It is written in the function definition.


def add(a, b):
  return a + b


print(add(6, 7))


# Default Values
def product(a=1, b=1):
  print(a * b)


product(4)
product(4, 5)


# Position of the Parameters
def sub(a, b):
  print(a - b)


sub(b=6, a=2)

